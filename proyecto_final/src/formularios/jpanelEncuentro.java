    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int puntosE1, puntosE2, golesE1, golesE2, ptoAcumE1, ptoAcumE2;
        String E1,E2, estadio, fecha, tipo, area, grupo;
        ptoAcumE1 = 0;
        ptoAcumE2 = 0;
        
        tipo = jcbTipoPartido.getSelectedItem().toString();
        area = jcbArea.getSelectedItem().toString();
        grupo = jcbGrupo.getSelectedItem().toString();
        estadio = jcbEstadio.getSelectedItem().toString();
        fecha = jcbDia.getSelectedItem().toString() +"/"+ jcbMes.getSelectedItem().toString() +"/"+ jcbAnio.getSelectedItem().toString();
        
        //equipo 1
        E1 = jcbSeleccion1.getSelectedItem().toString();
        golesE1 = Integer.parseInt(txtGoles1.getText()) ;
        ptoAcumE1 = Integer.parseInt(lblPuntos1.getText());
        //equipo 2
        E2 = jcbSeleccion2.getSelectedItem().toString();
        golesE2 = Integer.parseInt(txtGoles2.getText()) ;
        ptoAcumE2 = Integer.parseInt(lblPuntos2.getText());
        
        if (tipo.equals("Oficial"))
        {
            if(golesE1 > golesE2)
            {
                JOptionPane.showMessageDialog(null, "El ganador del encuentro es la selección de: "+E1,"Ganador",JOptionPane.INFORMATION_MESSAGE);
                ptoAcumE1 = ptoAcumE1 + 3;
                ptoAcumE2 = ptoAcumE2 + 1;
                txtPuntos1.setText(Integer.toString(ptoAcumE1));
                txtPuntos2.setText(Integer.toString(ptoAcumE2));
            }
            if(golesE1 < golesE2)
            {
                JOptionPane.showMessageDialog(null, "El ganador del encuentro es la selección de: "+E2,"Ganador",JOptionPane.INFORMATION_MESSAGE);
                ptoAcumE1 = ptoAcumE1 + 1;
                ptoAcumE2 = ptoAcumE2 + 3;
                txtPuntos1.setText(Integer.toString(ptoAcumE1));
                txtPuntos2.setText(Integer.toString(ptoAcumE2));
            }
            if(golesE1 == golesE2)
            {
                JOptionPane.showMessageDialog(null, "El encuentro quedó empatado! ","Ganador",JOptionPane.INFORMATION_MESSAGE);
                ptoAcumE1 = ptoAcumE1 + 1;
                ptoAcumE2 = ptoAcumE2 + 1;
                txtPuntos1.setText(Integer.toString(ptoAcumE1));
                txtPuntos2.setText(Integer.toString(ptoAcumE2));
            }
        }else
        {
            ptoAcumE1 = ptoAcumE1 + 0;
            ptoAcumE2 = ptoAcumE2 + 0;
            txtPuntos1.setText(Integer.toString(ptoAcumE1));
            txtPuntos2.setText(Integer.toString(ptoAcumE2));
        }
        
       cn.guardar_encuentro(E1, E2, ptoAcumE1, ptoAcumE2, estadio, fecha, tipo, golesE1, golesE2, area, grupo);
       cn.actualizar_puntos(ptoAcumE1, E1);
       cn.actualizar_puntos(ptoAcumE2, E2);
       
       
       
    }//GEN-LAST:event_jButton1ActionPerformed

