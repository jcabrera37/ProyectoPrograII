    public void mostrarComboSelecciones(){
        String query = "SELECT seleccion, Estadio FROM seleccion;";
        
         try {
            con = cn.getConection();
            st = con.createStatement();
            rs = st.executeQuery(query);
            
             while (rs.next()) {
                 jcbSeleccion1.addItem((String) rs.getObject("seleccion"));
                 jcbSeleccion2.addItem((String) rs.getObject("seleccion"));
                 jcbEstadio.addItem((String) rs.getObject("Estadio"));
             }
                       
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

