   public int mostrarInfoEquipo(String seleccion){
       String query = "SELECT Estadio, puntos_jornada FROM `seleccion` WHERE seleccion = '"+seleccion+"'";
       
       try {
           con = cn.getConection();
            st = con.createStatement();
            rs = st.executeQuery(query);
            
             while (rs.next()) {
                Estadio = rs.getString("Estadio");
                puntos_jornada = rs.getInt("puntos_jornada");
                
             }
       } catch (Exception e) {
       }
       return puntos_jornada;
   }

