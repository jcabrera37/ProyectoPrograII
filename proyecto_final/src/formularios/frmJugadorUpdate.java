    private void jbtnGuardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnGuardar1ActionPerformed
        
        //elegir seleccion
        seleccionar();
        //elige tipo de jugador/posicion
        seleccionarTipo();
        
        String nombre = txtNombre.getText();
        String apellidos = txtApellidos.getText();
        int camisola =  Integer.parseInt(txtCamisola.getText());
        int edad = Integer.parseInt(txtEdad.getText());
        String habilidad = txtHabilidades.getText();
        String p_habil =  jcbPierna.getSelectedItem().toString();
        String nac = jcbNacionalizado.getSelectedItem().toString();
        String mPat = txtMarca.getText();
        int valorMerc = Integer.parseInt(txtValorMercado.getText());
        
        String img_foto = "";        
        
        Jugador j = new Jugador();
        j.setNombre(nombre);
        j.setApellidos(apellidos);
        j.setImg_foto(img_foto);
        j.setEdad(edad);
        j.setCamisola(camisola);
        j.setHabilidad(habilidad);
        j.setPierna_habil(p_habil);
        j.setNacionalizado(nac);
        j.setId_seleccion(idSeleccion);
        j.setTipo(idTipo);
        j.setPatrocinador(mPat);
        j.setValor_mercado(valorMerc);
        
        guardarJugador(nombre, apellidos, img_foto,edad,camisola,habilidad,p_habil,nac,idSeleccion, idTipo, mPat,valorMerc);
        System.out.println("Registro realizado con exito!");
        
        String query = "SELECT MAX(id_jugador) AS id_jugador FROM jugador";
        
         try {
            con = cn.getConection();
            st = con.createStatement();
            rs = st.executeQuery(query);
        
            while (rs.next()) {
                uId = rs.getInt("id_jugador");
            }
            //System.out.println(uId);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
        
        ConexionBD conn = new ConexionBD();
       
        String url = lblruta.getText();
        if(url.trim().length() != 0 && nombre.trim().length() != 0){
            conn.guardarFotoJugador(ruta,uId);
            System.out.println("La foto se guardó exitosamente");
        }
        else{
            JOptionPane.showMessageDialog(null, "No debe dejar los campos vacios: Nombre e Imagen");
        }
        
        limpiarJtext();
       
    }//GEN-LAST:event_jbtnGuardar1ActionPerformed

