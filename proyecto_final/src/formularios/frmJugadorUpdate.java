    public void convertirSeleccion(String seleccion){
        
        String query = "SELECT id_seleccion, seleccion FROM `seleccion`\n" +
                    "WHERE seleccion = '"+seleccion+"';";
         try {
            con = cn.getConection();
            st = con.createStatement();
            rs = st.executeQuery(query);
        
            while (rs.next()) {
                idSeleccion = rs.getInt("id_seleccion");
            }
            //System.out.println(idSeleccion);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }
    
