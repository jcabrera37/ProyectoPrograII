    public void mostrarDatos(int id){
       
        
        String query = "SELECT id_jugador, nombre, apellidos, tipo, seleccion\n" +
"FROM jugador\n" +
"INNER JOIN tipo_jugador\n" +
"on jugador.id_tipo = tipo_jugador.id_tipo\n" +
"INNER JOIN seleccion\n" +
"ON jugador.id_seleccion = seleccion.id_seleccion\n" +
"WHERE jugador.id_seleccion = "+id+"";
        System.out.println(query);
         try {
            con = cn.getConection();
            st = con.createStatement();
            rs = st.executeQuery(query);
        
            Object[] usuarios = new Object[12];
            modelo = (DefaultTableModel) tablaUsuarios.getModel();
            while (rs.next()) {
                usuarios[0] = rs.getInt("id_jugador");
                usuarios[1] = rs.getString("nombre");
                usuarios[2] = rs.getString("apellidos");
                usuarios[3] = rs.getString("tipo");
                usuarios[4] = rs.getString("seleccion");
                
                modelo.addRow(usuarios);
            }
            tablaUsuarios.setModel(modelo);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        

    }//fin metodo mostrar
    
    
