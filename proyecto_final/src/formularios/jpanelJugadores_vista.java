    public void mostrarComboSelecciones(){
        String query = "SELECT seleccion FROM seleccion;";
        
         try {
            con = cn.getConection();
            st = con.createStatement();
            rs = st.executeQuery(query);
            
             while (rs.next()) {
                 jcbSelecciones.addItem((String) rs.getObject("seleccion"));
             }
                       
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    
    public int seleccionarIdSeleccion(){
        String selec_buscar;
        selec_buscar = jcbSelecciones.getSelectedItem().toString();
       
        String query2 = "SELECT id_pais FROM seleccion WHERE seleccion = '"+selec_buscar+"'";
        
        //System.out.println(selec_buscar);
        try {
            con = cn.getConection();
            st = con.createStatement();
            rs = st.executeQuery(query2);
           
            
            while (rs.next()) {
               id_encontrado = rs.getInt("id_pais");
               
            }
            //System.out.println(id_encontrado);
            
        } catch (Exception e) {
            System.out.println(e);
        }
        return id_encontrado;
        
    }
    
     public void limpiarModelo(){
        for (int i = 0; i < tablaUsuarios.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }//fin metodo limpiartabla
