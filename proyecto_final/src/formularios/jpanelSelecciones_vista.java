    //Metodo mostrar selecciones en combobox
    public void mostrarComboSelecciones(){
        
        String query = "SELECT Nombre FROM pais";
        
         try {
            con = cn.getConection();
            st = con.createStatement();
            rs = st.executeQuery(query);
            
             while (rs.next()) {
                 jcbPais.addItem((String) rs.getObject("Nombre"));
             }
                       
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
