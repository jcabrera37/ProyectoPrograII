    //METODO OBTENER ID DE PAIS
    public int obtenerIdPais(String nombrePais){
        
        String query = "SELECT id_pais, Nombre FROM `pais`\n" +
                    "WHERE Nombre = '"+nombrePais+"';";
         try {
            con = cn.getConection();
            st = con.createStatement();
            rs = st.executeQuery(query);
        
            while (rs.next()) {
                idPais = rs.getInt("id_pais");
            }
            //System.out.println(idSeleccion);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return idPais;
    }
    
    
